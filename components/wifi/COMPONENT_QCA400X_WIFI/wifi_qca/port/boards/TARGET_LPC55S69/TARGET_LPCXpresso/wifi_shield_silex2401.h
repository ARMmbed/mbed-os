#   error "Other WiFi shield is already enabled !"
#endif

#define MIKROE  0x1  // Wifi board connects to the mikroe connector
#define ARDUINO 0x2  // Wifi board connects to the arduino connector

void BOARD_InitSilex2401Shield(void);

/* Pinmux function, generated by pinmuxtool */
#define WIFISHIELD_PINMUX_INIT BOARD_InitSilex2401Shield

#define BOARD_INITSILEX2401SHIELD_PWRON_GPIO GPIO
#define BOARD_INITSILEX2401SHIELD_PWRON_PORT 1U
